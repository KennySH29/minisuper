CREATE TABLE ROL (
    ID_ROL NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR2(50) NOT NULL UNIQUE
);

CREATE TABLE METODO_PAGO (
    ID_METODO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DESCRIPCION VARCHAR2(50) NOT NULL UNIQUE
);

CREATE TABLE CLIENTE (
    ID_CLIENTE NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR2(100) NOT NULL,
    CEDULA VARCHAR2(20) NOT NULL,
    APELLIDO1 VARCHAR2(100) NOT NULL,
    APELLIDO2 VARCHAR2(100),
    TELEFONO VARCHAR2(20),
    CORREO VARCHAR2(100)
);

CREATE TABLE PROVEEDOR (
    ID_PROVEEDOR NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE_EMPRESA VARCHAR2(100) NOT NULL,
    --CONTACTO VARCHAR2(100),
    TELEFONO VARCHAR2(20),
    CORREO VARCHAR2(100),
    DIRECCION VARCHAR2(200)
);

CREATE TABLE CATEGORIA (
    ID_CATEGORIA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR2(50) NOT NULL UNIQUE,
    DESCRIPCION VARCHAR2(150)
);

-- =========================================================
-- TABLAS RELACIONALES
-- =========================================================

CREATE TABLE USUARIO (
    ID_USUARIO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR2(100) NOT NULL,
    APELLIDO1 VARCHAR2(100) NOT NULL,
    APELLIDO2 VARCHAR2(100),
    CORREO VARCHAR2(100) NOT NULL UNIQUE,
    CLAVE VARCHAR2(100) NOT NULL,
    ID_ROL NUMBER NOT NULL,
    CONSTRAINT FK_USUARIO_ROL
        FOREIGN KEY (ID_ROL) REFERENCES ROL(ID_ROL)
);

CREATE TABLE PRODUCTO (
    ID_PRODUCTO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR2(100) NOT NULL,
    DESCRIPCION VARCHAR2(200),
    PRECIO NUMBER(10,2) NOT NULL,
    ID_CATEGORIA NUMBER NOT NULL,
    ID_PROVEEDOR NUMBER NOT NULL,
    CONSTRAINT FK_PRODUCTO_CATEGORIA
        FOREIGN KEY (ID_CATEGORIA) REFERENCES CATEGORIA(ID_CATEGORIA),
    CONSTRAINT FK_PRODUCTO_PROVEEDOR
        FOREIGN KEY (ID_PROVEEDOR) REFERENCES PROVEEDOR(ID_PROVEEDOR),
    CONSTRAINT CK_PRODUCTO_PRECIO
        CHECK (PRECIO >= 0)
);

CREATE TABLE INVENTARIO (
    ID_INVENTARIO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_PRODUCTO NUMBER NOT NULL,
    CANTIDAD_DISPONIBLE NUMBER NOT NULL,
    STOCK_MINIMO NUMBER NOT NULL,
    FECHA_ACTUALIZACION DATE DEFAULT SYSDATE,
    CONSTRAINT FK_INVENTARIO_PRODUCTO
        FOREIGN KEY (ID_PRODUCTO) REFERENCES PRODUCTO(ID_PRODUCTO),
    CONSTRAINT CK_INVENTARIO_CANTIDAD
        CHECK (CANTIDAD_DISPONIBLE >= 0),
    CONSTRAINT CK_INVENTARIO_STOCK
        CHECK (STOCK_MINIMO >= 0)
);

-- =========================================================
-- TABLAS TRANSACCIONALES
-- =========================================================

CREATE TABLE TBL_VENTA (
    ID_VENTA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FECHA_VENTA DATE DEFAULT SYSDATE NOT NULL,
    ID_CLIENTE NUMBER,
    ID_USUARIO NUMBER NOT NULL,
    ID_METODO NUMBER NOT NULL,
    TOTAL NUMBER(10,2) NOT NULL,
    CONSTRAINT FK_VENTA_CLIENTE
        FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTE(ID_CLIENTE),
    CONSTRAINT FK_VENTA_USUARIO
        FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO(ID_USUARIO),
    CONSTRAINT FK_VENTA_METODO
        FOREIGN KEY (ID_METODO) REFERENCES METODO_PAGO(ID_METODO),
    CONSTRAINT CK_VENTA_TOTAL
        CHECK (TOTAL >= 0)
);

CREATE TABLE TBL_DETALLE_VENTA (
    ID_DETALLE_VENTA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_VENTA NUMBER NOT NULL,
    ID_PRODUCTO NUMBER NOT NULL,
    CANTIDAD NUMBER NOT NULL,
    PRECIO_UNITARIO NUMBER(10,2) NOT NULL,
    SUBTOTAL NUMBER(10,2) NOT NULL,
    CONSTRAINT FK_DETALLE_VENTA_VENTA
        FOREIGN KEY (ID_VENTA) REFERENCES TBL_VENTA(ID_VENTA),
    CONSTRAINT FK_DETALLE_VENTA_PRODUCTO
        FOREIGN KEY (ID_PRODUCTO) REFERENCES PRODUCTO(ID_PRODUCTO),
    CONSTRAINT CK_DETALLE_VENTA_CANTIDAD
        CHECK (CANTIDAD > 0),
    CONSTRAINT CK_DETALLE_VENTA_PRECIO
        CHECK (PRECIO_UNITARIO >= 0),
    CONSTRAINT CK_DETALLE_VENTA_SUBTOTAL
        CHECK (SUBTOTAL >= 0)
);

CREATE TABLE COMPRA (
    ID_COMPRA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FECHA_COMPRA DATE DEFAULT SYSDATE NOT NULL,
    ID_PROVEEDOR NUMBER NOT NULL,
    ID_USUARIO NUMBER NOT NULL,
    TOTAL NUMBER(10,2) NOT NULL,
    CONSTRAINT FK_COMPRA_PROVEEDOR
        FOREIGN KEY (ID_PROVEEDOR) REFERENCES PROVEEDOR(ID_PROVEEDOR),
    CONSTRAINT FK_COMPRA_USUARIO
        FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO(ID_USUARIO),
    CONSTRAINT CK_COMPRA_TOTAL
        CHECK (TOTAL >= 0)
);

CREATE TABLE DETALLE_COMPRA (
    ID_DETALLE_COMPRA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_COMPRA NUMBER NOT NULL,
    ID_PRODUCTO NUMBER NOT NULL,
    CANTIDAD NUMBER NOT NULL,
    COSTO_UNITARIO NUMBER(10,2) NOT NULL,
    SUBTOTAL NUMBER(10,2) NOT NULL,
    CONSTRAINT FK_DETALLE_COMPRA_COMPRA
        FOREIGN KEY (ID_COMPRA) REFERENCES COMPRA(ID_COMPRA),
    CONSTRAINT FK_DETALLE_COMPRA_PRODUCTO
        FOREIGN KEY (ID_PRODUCTO) REFERENCES PRODUCTO(ID_PRODUCTO),
    CONSTRAINT CK_DETALLE_COMPRA_CANTIDAD
        CHECK (CANTIDAD > 0),
    CONSTRAINT CK_DETALLE_COMPRA_COSTO
        CHECK (COSTO_UNITARIO >= 0),
    CONSTRAINT CK_DETALLE_COMPRA_SUBTOTAL
        CHECK (SUBTOTAL >= 0)
);

CREATE TABLE BITACORA (
    ID_BITACORA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FECHA_EVENTO DATE DEFAULT SYSDATE NOT NULL,
    ACCION VARCHAR2(100) NOT NULL,
    DESCRIPCION VARCHAR2(300),
    ID_USUARIO NUMBER NOT NULL,
    CONSTRAINT FK_BITACORA_USUARIO
        FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO(ID_USUARIO)
);

